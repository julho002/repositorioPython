#Faça um Programa que leia três números e mostre o maior e o menor deles.

num1=int(input('Digite o primeiro número:'))
num2=int(input('Digite o primeiro número:'))
num3=int(input('Digite o primeiro número:'))

maior_numero=max(num1,num2,num3)
print('O MAIOR NÚMERO :',maior_numero)
menor_numero=min(num1,num2,num3)
print('O MENOR NÚMERO:',menor_numero)
